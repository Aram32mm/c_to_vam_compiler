// Example demonstrating basic arithmetic operations
int main() {
    int a = 10;
    int b = 5;
    int result;
    
    // Addition
    result = a + b;
    
    // Subtraction
    result = a - b;
    
    // Multiplication
    result = a * b;
    
    // Division
    result = a / b;
    
    return result;
}